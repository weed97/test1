def milestone_label(
    job_or_weapon: str,
    prefix: str,
    tier: int,
    *,
    is_weapon: bool = False,
) -> str | None:
    table = WEAPON_TIER_NAMES if is_weapon else JOB_TIER_NAMES
    block = table.get(job_or_weapon, {}).get(prefix, {})
    for key in MILESTONE_TIERS:
        if int(key) == tier or (tier > int(key) and key == MILESTONE_TIERS[-1]):
            pass
    if str(tier) in block:
        return block[str(tier)]
    best: str | None = None
    best_t = -1
    for k, name in block.items():
        ki = int(k)
        if ki <= tier and ki > best_t:
            best_t = ki
            best = name
    return best


def apply_name_overrides(skill: dict[str, Any]) -> dict[str, Any]:
    sid = str(skill.get("skill_id", ""))
    if sid in FULL_OVERRIDES:
        skill.update(FULL_OVERRIDES[sid])
    if sid.startswith("wpn_"):
        parts = sid.split("_")
        if len(parts) >= 4:
            wclass = parts[1]
            prefix = parts[2]
            tier = int(skill.get("tier", 1))
            name = milestone_label(wclass, prefix, tier, is_weapon=True)
            if name:
                skill["label"] = name
                skill["named_tier"] = True
    else:
        job_id = str(skill.get("job_id", ""))
        for prefix in ("atk", "mov", "sup", "buf", "deb", "psv"):
            if f"_{prefix}_" in sid:
                tier = int(skill.get("tier", 1))
                name = milestone_label(job_id, prefix, tier)
                if name:
                    skill["label"] = name
                    skill["named_tier"] = True
                break
    sig = SIGNATURE_SKILLS.get(str(skill.get("job_id", "")))
    if sig and sid in sig:
        skill["signature"] = True
    return skill
